#pragma once
#include <ShlObj.h>
#include <Windows.h>

#ifndef PCTSTR
#define PCTSTR LPCSTR
#endif
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////



////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// 使用方法
// 1. 指定文件夹位置 (CSIDL_*) 和快捷方式文件名称即可。
// 2. 特别地，如果文件夹位置为 CSIDL_APPDATA 将会转换成快速启动栏的文件夹位置。
// 3. 使用之前需要初始化 COM 库 (如 CoInitialize(NULL))。
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////



////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// CShortcut 类
class CShortcut
{
public:
	// 获取特殊文件夹路径
	static BOOL GetFolder(INT iFolder, PTSTR ptzFolder);

	// 判断快捷方式是否存在
	static BOOL Exist(INT iFolder, PCTSTR ptzName);

	// 创建快捷方式
	static BOOL Create(INT iFolder, PCTSTR ptzName, PCTSTR ptzDesc = NULL);

	// 删除快捷方式
	static BOOL Remove(INT iFolder, PCTSTR ptzName);
};
